public class Numbers {
    private int a;
    private int b;
    private int n;

    public Numbers(int a, int b, int n) {
        this.a = a;
        this.b = b;
        this.n = n;
    }

    public Numbers() { }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }
}
